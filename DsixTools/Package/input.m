If[wrongInput,

InputFixed=True;

(* Impose Hermiticity and obtain the independent WCs *)
WCtemp=Table[WCnew[m,n]/.checkAction[cat],{m,n1},{n,n2}]/.WC[m_,n_]:>WCtemp[[m,n]];

(* Obtain the values of the dependent WCs in the symmetric basis *)
WCtemp=WCtemp/.WCnew->XX/.SubRedu[cat]/.(XX[m_,n_]:>WCput[[m,n]]);

];

];

If[6<=cat<=13&&cat!=9&&cat!=11, (* 4F object *)

{n1,n2,n3,n4}=Dimensions[WCput];
checkConds4F[cat]=checkConds[cat]/.WC[m_,n_,p_,q_]:>If[m>n1||n>n2||p>n3||q>n4,0,WC[m,n,p,q]];

(* cats 9 and 11 do not require any check whereas cats 10, 12 and 13 must only be checked for antisymmetry, not for hermiticity *)

Do[
If[!(checkConds4F[cat][[k]]/.WC[m_,n_,p_,q_]:>WCput[[m,n,p,q]]),
wrongInput=True;
wrongWC=name<>"["<>ToString[GetArgs[checkConds[cat][[k]]][[1]]]<>","<>ToString[GetArgs[checkConds[cat][[k]]][[2]]]<>","<>ToString[GetArgs[checkConds[cat][[k]]][[3]]]<>","<>ToString[GetArgs[checkConds[cat][[k]]][[4]]]<>"]";
oldValue=checkConds[cat][[k,1]]/.WC[i_,j_,k_,l_]:>WCtemp[[i,j,k,l]];
If[6<=cat<=8,
AppendTo[NonHermitianErrors,{wrongWC,oldValue}]; (* Non-Hermitian input *)
,
AppendTo[AntisymmetryErrors,{wrongWC,oldValue}]; (* Non-vanishing symmetric element in antisymmetric object *)
];
];
,{k,1,Length[checkConds[cat]]}];

If[wrongInput,

InputFixed=True;

(* Impose Hermiticity and obtain the independent WCs *)
WCtemp=Table[WCnew[m,n,p,q]/.checkAction[cat],{m,n1},{n,n2},{p,n3},{q,n4}]/.WC[m_,n_,p_,q_]:>WCtemp[[m,n,p,q]];

(* Obtain the values of the dependent WCs in the symmetric basis *)
WCtemp=WCtemp/.WCnew->XX/.SubRedu[cat]/.(XX[m_,n_,p_,q_]:>WCput[[m,n,p,q]]);

];

];

Return[WCtemp];
]


