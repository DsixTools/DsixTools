InputFixed=False;
];

,
Message[ChangeInput::MixedEFTs];
];
,
Do[
If[!MemberQ[Join[SMEFTParametersTotal,LEFTParametersTotal],list[[i,1]]],Message[ChangeInput::WrongParameter,list[[i,1]]];];
,{i,Length[list[[All,1]]]}];
];
];
