ChangeInput[list_,dispatch_,dispatchPre_]:=Block[{param,newvalue,objlist,pos,obj,cat,newobj,paramLoop,newvalueLoop,listgrouped,hparam},

(* Check all input parameters *)
If[MyContainsAll[Join[SMEFTParametersTotal,LEFTParametersTotal],list[[All,1]]],

(* Determine EFT *)
If[MyContainsAll[SMEFTParametersTotal,list[[All,1]]]||MyContainsAll[LEFTParametersTotal,list[[All,1]]],

If[MyContainsAll[SMEFTParametersTotal,list[[All,1]]],
(* SMEFT *)
objlist=SMEFTObjectList;
,
(* LEFT *)
objlist=LEFTObjectList;
];

(* Group by blocks with the same object *)
listgrouped=GatherBy[list,Head[#[[1]]]&];

Do[

If[Length[listgrouped[[k]]]==1, (* no repeated object *)

param=listgrouped[[k,1,1]];
newvalue=listgrouped[[k,1,2]];

(* We determine the position in ObjectList and find the corresponding object and its category *)
hparam=param/.XX_[i_,j_,k_,l_]:>XX/.XX_[i_,j_]:>XX;
pos=Position[objlist[[All,2]],hparam][[1,1]];
obj=objlist[[pos,1]];
cat=objlist[[pos,4]];

(* Intermediate input *)
dispatchPre=dispatchPre/.{(param->(param/.dispatch))->(param->newvalue)};

, (* repeated object *)

Do[

param=listgrouped[[k,k2,1]];
newvalue=listgrouped[[k,k2,2]];

(* We determine the position in ObjectList and find the corresponding object and its category *)
hparam=param/.XX_[i_,j_,k_,l_]:>XX/.XX_[i_,j_]:>XX;
pos=Position[objlist[[All,2]],hparam][[1,1]];
obj=objlist[[pos,1]];
cat=objlist[[pos,4]];

(* Intermediate input *)
dispatchPre=dispatchPre/.{(param->(param/.dispatch))->(param->newvalue)};

,{k2,1,Length[listgrouped[[k]]]}];

];

(* We check and symmetrize the object *)
newobj=CheckAndSymmetrize[obj/.dispatchPre,cat,ToString[MyHead[param]]];

(* New list with replaced values *)
If[Length[obj]!=0,  (* Object with indices *)
Do[

paramLoop=Flatten[obj][[i]];
newvalueLoop=Flatten[newobj][[i]];
dispatchPre=dispatchPre/.{(paramLoop->(paramLoop/.dispatchPre))->(paramLoop->newvalueLoop)};

,{i,1,Length[Flatten[obj]]}];
