ComputeSMEFTrunnerInd:=Block[{},
If[!ValueQ[ParametersSMEFTInd],ParametersSMEFTInd=ToIndependentSingle/@ParametersSMEFT;];
Return[Chop[SMEFTrun/@ParametersSMEFTInd/.\[Mu]->EWSCALE,$MachineEpsilon]];
];


