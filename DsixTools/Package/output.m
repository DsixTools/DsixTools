outfile=OpenWrite[outname,FormatType->StandardForm];

(* Write output blocks *)
WriteScales["SMEFTrunner"];
WriteSM;
WriteWCs["SMEFT"];
WriteOptions;

(* Close file *)
Close[outfile];
];


(* ::Input::Initialization:: *)
WriteLEFTOutputFile[outname_,dataOutput_,option_]:=Block[{outfile},

(* Open file *)
outfile=OpenWrite[outname,FormatType->StandardForm];

(* Write output blocks *)
If[option==1,WriteScales["EWmatcher"];];
If[option==2,WriteScales["LEFTrunner"];];
WriteQCDQED;
WriteWCs["LEFT"];
WriteOptions;

(* Close file *)
Close[outfile];
];


(* ::Input::Initialization:: *)
WriteWCXFOutputFile[outname_,format_,EFT_,scale_]:=Block[{outfile},

(* Open file *)
outfile=OpenWrite[outname,FormatType->StandardForm];

If[format=="JSON"&&EFT=="SMEFT",WriteWCsSMEFTJSON[scale];];
If[format=="JSON"&&EFT=="LEFT",WriteWCsLEFTJSON[scale];];
If[format=="YAML"&&EFT=="SMEFT",WriteWCsSMEFTYAML[scale];];
If[format=="YAML"&&EFT=="LEFT",WriteWCsLEFTYAML[scale];];

(* Close file *)
Close[outfile];
];


(* Printing function *)
MyPrint[string_]:=Block[{},
If[PrintMessages,Print[string]];
];


TurnOnMessages:=Block[{},
PrintMessages=True;
];


TurnOffMessages:=Block[{},
PrintMessages=False;
];


(* ::Input::Initialization:: *)
SMEFTrunnerExport:=Block[{},

Which[
Length[{##}]==0,
MyPrint["Exporting SMEFTrunner results"];
dataOutput=Chop[SMEFTrun/@ParametersSMEFT/.\[Mu]->EWSCALE,$MachineEpsilon];
outname="Output_SMEFTrunner.dat";
WriteSMEFTOutputFile[outname,dataOutput];
,
Length[{##}]==2,
MyPrint["Exporting SMEFTrunner results"];
If[{##}[[1]]=="SLHA",
dataOutput=Chop[SMEFTrun/@ParametersSMEFT/.\[Mu]->EWSCALE,$MachineEpsilon];
outname={##}[[2]]<>".dat";
WriteSMEFTOutputFile[outname,dataOutput];
,
dataOutput=ComputeSMEFTrunnerInd;
If[{##}[[1]]=="JSON",outname={##}[[2]]<>".json";];
If[{##}[[1]]=="YAML",outname={##}[[2]]<>".yaml";];
WriteWCXFOutputFile[outname,{##}[[1]],"SMEFT",EWSCALE];
];
,
Length[{##}]!=0&&Length[{##}]!=2,
Message[SMEFTrunnerExport::WrongInput]
];

]&;


