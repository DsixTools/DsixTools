WriteWC2F[nameblock_,name_,n_,cat_,ReIm_,n1_,n2_]:=Block[{},

Write[outfile,"Block "<>nameblock];
If[cat==1,indices=indices1;indicesIm=indices;];  (* general 3x3 matrix *)
If[cat==2&&ReIm==0,indices=indices2Re;]; (* Hermitian 3x3 matrix - Real part *)
If[cat==2&&ReIm==1,indices=indices2Re;indicesIm=indices2Im;]; (* Hermitian 3x3 matrix - Imaginary part *)
If[cat==3,indices=indices3;indicesIm=indices;];  (* symmetric 3x3 matrix *)
If[cat==4,indices=indices4;indicesIm=indices;];  (* antisymmetric 3x3 matrix *)

indices=ToGens2F[indices,n1,n2];
indicesIm=ToGens2F[indicesIm,n1,n2];

Do[
If[ReIm==0&&IntersectingQ[{{i,j}},indices],
k=Position[indices,{i,j}][[1,1]];
Write[outfile,i," ",j," ",outR[Re[dataOutput[[n-1+k]]]],"\t","# "<>name<>"["<>ToString[i]<>","<>ToString[j]<>"]"];
];
If[ReIm==1&&IntersectingQ[{{i,j}},indicesIm],
k=Position[indices,{i,j}][[1,1]];
Write[outfile,i," ",j," ",outR[Im[dataOutput[[n-1+k]]]],"\t","# "<>name<>"["<>ToString[i]<>","<>ToString[j]<>"]"];
];
,{i,1,3},{j,1,3}];
];


